/**
 * Device screen information 
 */
interface DeviceScreen {
  /** 
   * Get the device exact screen height
   */
  String get height();
  
  /**
   * Get the device's exact screen width
   */
  String get width();
  
  /**
   * Get the device's operating system name.
   */
  String get colorDepth();
}


